# In lists you can store information in an order

ranks = ["Radiant", "Immortal", "Diamond",
         "Platinum", "Gold", "Silver", "Bronze", "Iron"]

# With a loop you can iterate through the list
for rank in ranks:
    print(rank)

# You can also manipulate the output
for rank in ranks:
    print(rank.lower())

